// Bibliotecas embutidas
const ValorAbsoluto = Math.abs(-5) // Retorna o valor absoluto do número
console.log(`O valor absoluto de -5 é ${ValorAbsoluto}`)
const raizQuadrada = Math.sqrt(16) // Retorna a raiz quadrada
console.log(`A raiz quadrada de 16 é ${raizQuadrada}`)
const potencia = Math.pow(2, 3) // Retorna 2 elevado a potência de 3 = 8
console.log(`A potência de 2 é ${potencia}`)
const aleatorio = Math.random() // retorna um número aleatório quebrado entre 0 e 1
console.log(`O número aleatório é ${aleatorio}`)